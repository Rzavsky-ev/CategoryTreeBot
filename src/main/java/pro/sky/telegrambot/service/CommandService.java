package pro.sky.telegrambot.service;

import com.pengrad.telegrambot.model.Update;

/**
 * Сервис обработки команд Telegram бота.
 * <p>
 * Определяет основной интерфейс для обработки входящих команд от пользователей.
 * Получает обновления от Telegram API и делегирует их обработку соответствующим обработчикам команд.
 *
 * @see Update Класс, представляющий входящее обновление от Telegram API
 */
public interface CommandService {

    /**
     * Обрабатывает входящую команду из обновления Telegram.
     * <p>
     * Реализация должна:
     * <ul>
     *     <li>Определять тип входящего обновления (сообщение, callback и т.д.)</li>
     *     <li>Извлекать команду и её аргументы</li>
     *     <li>Находить соответствующий обработчик команды</li>
     *     <li>Передавать управление обработчику команды</li>
     *     <li>Обрабатывать возможные ошибки выполнения</li>
     * </ul>
     *
     * @param update входящее обновление от Telegram API, содержащее команду
     */
    void processCommand(Update update);
}
